package com.mikedeejay2.mikedeejay2lib.chat;

/**
 * Stores the type of data that <tt>ChatText</tt>
 * is holding. <p>
 * This enum holds:
 * <ul>
 *     <li>NORMAL - A normal </li>
 *     <li>CLICKABLE - A clickable piece of text </li>
 *     <li>HOVERABLE - A hoverable piece of text </li>
 *     <li>HOVERCLICKABLE - A hoverable and clickable piece of text </li>
 * </ul>
 *
 * @see ChatText
 *
 * @author Mikedeejay2
 */
public enum ChatTextType
{
    NORMAL,
    CLICKABLE,
    HOVERABLE,
    HOVERCLICKABLE;
}
